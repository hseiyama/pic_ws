	CPU	68000
	SUPMODE	OFF

	ORG	$00008000

START:
	MOVE.L	#$00008080,A7
	MOVE.L	#$12345678,A6
	CLR.L	SUMVAL
LOOP:
	ADDQ.L	#8,A6
	BSR	SUMFNC
	BRA	LOOP
	ILLEGAL
;	STOP	#$0000

SUMFNC:
	LINK	A6,#-8
	MOVE.L	D0,-8(A6)
	ADDQ.L	#1,SUMVAL
	MOVE.L	SUMVAL,D0
	UNLK	A6
	RTS

	ORG	$00008040
	DC.B	[$00008080-*]$FF

	ORG	$00008040

SUMVAL:	DS.L	1

END
