					// FLO = 01: XON/XOFF Software flow control

// UART ring buffer init
	rx_xreq = 0;
	rx_xflg = XON;
	rx_wp = rx_rp = rx_cnt = 0;
	tx_wp = tx_rp = tx_cnt = 0;

	U3RXEN = 1;		// Receiver enable
	U3TXEN = 1;		// Transmitter enable

