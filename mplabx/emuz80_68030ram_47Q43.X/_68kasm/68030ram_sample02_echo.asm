	CPU	68030
	SUPMODE	ON

CR:	EQU	$0D
LF:	EQU	$0A
ESC	EQU	$1B		; ESC code

CONIN	EQU	$0001E4B2
CONOUT	EQU	$0001E4D2
STROUT	EQU	$0001D712

ENTRY	EQU	$000000C0	; Entry address

	ORG	ENTRY

ECHO_S:
	BSR	CONIN
	CMP.B	#ESC,D0
	BEQ	ECHO_E
	BSR	CONOUT
	BRA	ECHO_S
ECHO_E:
	LEA	CRLF,A0
	BSR	STROUT
BREAK:	DC.W	$4AFC

CRLF:	DC.B	CR,LF,$00

	END
